FILENAME = 'tmp_markov_model.maude'
TOPLEVELDIR = Path(os.path.dirname(__file__))
EXAMPLE_FILE_PATH = TOPLEVELDIR.joinpath(FILENAME)

def tokenize(text):
    """
    Simple tokenizer to enable comparison ignoring whitespace/formatting quirks.
    Splits on whitespace and captures basic tokens.
    """
    # Replace parentheses and commas with spaced versions to split easily
    text = text.replace("(", " ( ").replace(")", " ) ").replace(",", " , ")
    # Split by whitespace
    tokens = text.split()
    return tokens


def find_mismatch(gen_tokens, exp_tokens, context=5):
    """
    Finds the first mismatch between token lists and returns a descriptive error string.
    """
    limit = min(len(gen_tokens), len(exp_tokens))
    for i in range(limit):
        if gen_tokens[i] != exp_tokens[i]:
            start = max(0, i - context)
            end = min(limit, i + context)
            gen_snippet = " ".join(gen_tokens[start:end])
            exp_snippet = " ".join(exp_tokens[start:end])
            return (
                f"Mismatch at token index {i}:\n"
                f"  Expected token: '{exp_tokens[i]}'\n"
                f"  Actual token:   '{gen_tokens[i]}'\n\n"
                f"  Context (Expected): ...{exp_snippet}...\n"
                f"  Context (Actual):   ...{gen_snippet}..."
            )

    if len(gen_tokens) != len(exp_tokens):
        return f"Token count mismatch: Generated={len(gen_tokens)}, Expected={len(exp_tokens)}"

    return None


