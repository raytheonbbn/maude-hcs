def test_json_to_maude_generation():
    json_content = JSON_STRING
    expected_maude = EXPECTED_MAUDE_STRING

    # Generate Output
    parser = JsonToMaudeParser(model_name="dns", stem="dns", load_path="../../../common/maude/markov-action-model.maude")
    generated_maude = parser.generate(json_content)

    with open(EXAMPLE_FILE_PATH, 'w', encoding='utf-8') as f:
        f.write(generated_maude)

    # Tokenize both
    gen_tokens = tokenize(generated_maude)
    exp_tokens = tokenize(expected_maude)

    # Assert and provide detailed feedback on failure
    mismatch_error = find_mismatch(gen_tokens, exp_tokens)

    assert mismatch_error is None, f"Generated Maude code does not match expected output.\n{mismatch_error}"

